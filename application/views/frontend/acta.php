<p>
Los rematadores que suscriben, reunidos en la calle Bartolomé Mitre número setecientos
 cuarenta y seis, con el objeto de considerar el presente momento del gremio, han
 convenido en la necesidad de darle legítima y completa representación.<br /><br />
<b> A este efecto resuelven:</b></p>

<p><b> 1): </b>Dar por constituida la "Corporación de Rematadores" y refirmar al mismo
 tiempo el propósito de mantener el estricto cumplimiento de reglas de ética en los asuntos
 de su respectiva intervención.</p>
<p>
 <b>2): </b>Invitar a asociarse a los colegas que convinieren en iguales propósitos y cuyo
 desempeño profesional fuere de notoriedad.</p>

<p> <b>3):</b> Nombrar la siguiente Comisión Directiva:<br><br>

 Presidente Jacinto Peralta Martinez<br>
 Vice-Presidente Rufino de Elizalde<br>
 Secretario Alberto D. Justo<br>
 Tesorero Carlos S. Velarde<br>
 Vocal Alberto Gimenez Zapiola<br>
 Vocal Luis Gustavo Lanusse<br>
 Vocal Jose M. Bacigaluppi<br>
 </p>

 <p><b>4):</b> Facultar a la C.D:<br><br>
<b> a)</b> para designar hasta tres suplentes si lo creyere necesario;<br>
 <b>b)</b> para nombrar la comisión encargada de la redacción del estatuto social;<br>
<b> c)</b> para proveer la instalación de la Corporación y a las necesidades del adecuado funcionamiento
 de la misma, asi como a cuanto reuiera lo anunciado en la presente acta. A
 estos fines se establece que la C. D. podrá funcionar con un mínimo de cuatro miembros
 y tomar resoluciones a simple pluralidad de votos; el presidente tendrá voto en
 todos los casos, y en el de empate su voto será decisivo;<br>
<b> d) </b>para llamar a Asamblea y someter a su resolución cualquier asunto que la requiera.

<p> <b>5):</b> Fijar a titulo provisional una cuota de ingreso de pesos m/n por persona
 y una mensualidad de pesos m/n. Los fondos que por este concepto se
 recaudaren, como su manejo e inversiones, estarán a cargo del tesorero.Conformes en un todo con la presente, 
 firmamos en Buenos Aires, a veinticinco de Julio de mil novecientos treinta y uno.
</p>


                        
                        
                     
