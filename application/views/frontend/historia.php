                                <h3>ALBERTO ENRIQUE BIEULE</h3>
                                <hr>
                                <p>Se incorpora a la firma en el a&ntilde;o 1955, con Matricula de Martillero y Corredor de fecha 14-10-1960, continuando con la administraci&oacute;n de propiedades en Capital Federal, llegando a tener 90 edificios en propiedad horizontal, en su mayor&iacute;a ubicados en Barrio Norte.</p>
                                
                                <p>
En el a&ntilde;o 1970 se inscribe como Martillero judicial en el fuero Civil y en el Comercial, realizando subastas de importantes empresas en quiebra, como tambi&eacute;n ejecuciones hipotecarias a propuesta de parte, trabajando para los bancos de primera l&iacute;nea y gran cantidad de estudios jur&iacute;dicos de la Capital Federal.</p>

<p>
En el a&ntilde;o 1974, cesa la divisi&oacute;n  administraci&oacute;n de propiedades y continua con la inmobiliaria en Adrogu&eacute;, asociado con el Sr. Juan Carlos Della Paolera, hasta el a&ntilde;o 1995, que decide cerrar la oficina de Adrogu&eacute;, para concentrarse en las oficinas en la calle Lavalle 1290, C.A.B.A. 
Contratado desde 1993 hasta el 2001 con fines de asesoramiento y venta de bienes muebles del estado por el Ministerio de Econom&iacute;a de la Naci&oacute;n mediante licitaci&oacute;n p&uacute;blica por concurso de antecedentes. </p> 

