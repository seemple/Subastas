<div class="span5">
	
		
 <p>La <b>Corporación de Rematadores y Corredores Inmobiliarios</b> fue fundada el 25 de Julio de 1931. Tiene su sede en la calle Tte. Gral. Juan Domingo Perón 1233 de la Ciudad Autónoma de Buenos Aires, donde se llevan a cabo toda clase de remates, judiciales, oficiales y particulares. 
</p>
                        
                        
                         <p>En sus instalaciones se realizan todas las subastas dispuestas por los magistrados de la Capital Federal, sin excepción, desde que funciona en su sede la Oficina de Subastas Judiciales creada por la Corte Suprema de Justicia de la Nación. Esto ha generado que la Corporación tome mayor importancia en lo que refiere a venta de bienes, ya que durante el 2001 y 2002 realizó transacciones por mas de 200 millones de pesos, realizándose mensualmente mas de 400 subastas judiciales durante el 2001. Las instalaciones de la Institución están equipadas con las últimas herramientas para realización de las subastas, lo que permite que cada acto de remate sea registrado tanto por las cámaras de los salones como por las grabaciones de voz permitiendo una mayor transparencia y seriedad en la actividad. Sumado a esto la colocación de detectores de armas, y un sistema de seguridad permanente con presencia continua de efectivos de la Policía Federal Argentina dependiente directamente de la Suprema Corte. 
</p>

<p>Este crecimiento e innovación permanente en la actividad llevó a la institución a realizar un sitio de comunicación virtual serio, flexible, de última generación que permite comunicar las actividades que diariamente se llevan a cabo en su sede como así contribuir a una mayor propagación en este sistema de venta permitiendo transparencia e información constante de las distintas subastas a realizarse. </p>

</div>

<div class="span8">
    <p>En<b> www.rematescorporacion.org</b> se publican todas aquellas subastas a realizarse en esta institución. Los usuarios que ingresen tendrán la posibilidad de consultar estadísticas de toda índole, novedades y una importante gama de información disponible. La ventaja principal de este desarrollo es que estará al alcance de toda aquella persona que lo requiera, de manera gratuita y durante las 24 hs, brindando constantemente información actualizada y transparente sobre el mundo de los remates judiciales. Próximamente en esta sección encontrarán la información Institucional de la Corporación como así de sus directivos, de aquellos que lo hicieron a lo largo de la historia de la Institución, comunicados de prensa, todos los datos relativos a la actividad tanto, para los antiguos como los nuevos Profesionales del rubro.</p> 
    
    <p>La Corporación de Rematadores y Corredores Inmobiliarios le dan la bienvenida a esta nueva etapa en lo que se refiere a información de subastas.</p>
    
