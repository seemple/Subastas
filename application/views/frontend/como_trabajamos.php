                                <p>Cuando tomamos una propiedad en venta, <strong>realizamos los siguientes pasos:</strong></p>


								<h2>DOCUMENTACI&Oacute;N:</h2>
                                <p>Es conveniente contar con la copia del t&iacute;tulo de propiedad, planos de construcci&oacute;n, comprobantes de impuestos: A.B.L, A.R.B.A y expensas, dependiendo del tipo de inmueble. </p>

								<hr class="half-margins">

								<h2>ESTIMACI&Oacute;N de VALORES:</h2>
                                <p>Luego de la visita a la propiedad se confecciona la tasaci&oacute;n, que incluye un estudio de mercado con toda la documentaci&oacute;n recopilada y la estimaci&oacute;n del valor de realizaci&oacute;n</p>

								<hr class="half-margins">

								<h2>FICHA DE LA PROPIEDAD:</h2>
                                <p>Con la informaci&oacute;n suministrada por el propietario, la recabada por nosotros y la tasaci&oacute;n se confecciona la ficha en la que se incluyen todos los datos de la propiedad, fotos, planos, informaci&oacute;n, descripci&oacute;n del inmueble, impuestos y gastos de comercializaci&oacute;n. Todo lo cual ser&aacute; suministrado a los posibles interesados.</p>


								<hr class="half-margins">

								<h2>AUTORIZACI&Oacute;N DE VENTA:</h2>
                                <p>Este instrumento es fundamental para nuestra firma y para que la operaci&oacute;n se realice en forma clara y transparente. Aqu&iacute; quedaran establecidas las condiciones de venta, honorarios y las circunstancias particulares de cada operaci&oacute;n, estableciendo plazos para la firma del boleto, posterior escrituraci&oacute;n, monto de venta, bienes que incluyen o excluyen la operaci&oacute;n. La misma ser&aacute; por un tiempo determinado y se podr&aacute; cancelar previo aviso por escrito.</p>

