                                <p>Para el caso de la firma del boleto, en estos &uacute;ltimos tiempos ya casi no se utiliza este instrumento, se expedir&aacute;, en ese momento, un certificado de dominio para tranquilidad de los compradores. Tramite que se encarga nuestra firma personalmente. Se establecer&aacute;n las condiciones de la operaci&oacute;n, si la venta incluye o no tel&eacute;fono, cocheras, bauleras, los datos del escribano actuante domicilio y tel&eacute;fono. </p>

