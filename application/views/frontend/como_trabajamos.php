								<h2>RESERVA:</h2>
                                <p>Una vez tomada una oferta, que consideramos razonable, por un posible interesado, se confecciona la reserva, la cual llevara la posterior conformidad del vendedor, en caso de ser aceptada. En este instrumento adem&aacute;s de las condiciones de venta se dejara establecido si se firmar&aacute; un boleto de compra-venta o se pasara directamente a la escritura traslativa de dominio.</p>

